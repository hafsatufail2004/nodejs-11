const add = (a, b) => {
    let sum = a + b;
    return sum;
};
const subtract = (a, b) => {
    let sum0 = a - b;
    return sum0;
};
const multiply = (a, b) => {
    let sum1 = a * b;
    return sum1;
};
const divide = (a, b) => {
    let sum2 = a / b;
    return sum2;
};
export default add;
export { subtract, divide, multiply };
